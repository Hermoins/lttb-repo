def generate_gas_data(seed=42):
    """
    ================================================================================
    生成煤矿瓦斯浓度模拟数据
    ================================================================================
    
    功能说明：
        本函数生成符合煤矿实际瓦斯涌出特征的模拟数据集，用于验证
        动态LTTB算法在异常检测场景下的性能表现。
    
    数据特征：
        1. 平稳基线：正常开采时的瓦斯浓度均值约0.3%
        2. 周期性波动：采煤机周期性作业导致的呼吸效应
        3. 三种典型异常模式：
            - 模式A（突发尖峰）：放顶煤作业引起的瞬时瓦斯涌出
            - 模式B（缓慢涌出）：地质构造影响导致的渐进式涌出
            - 模式C（指数爆发）：揭穿瓦斯包引起的高速涌出
        4. 传感器噪声：高斯噪声 + 脉冲噪声
    
    参数说明：
        seed (int): 随机种子，用于保证实验可重复性，默认值42
    
    返回值：
        tuple: (时间序列t, 瓦斯浓度数据gas_data)
            - t: numpy.ndarray, 形状为(3600,), 时间点从0到3599秒
            - gas_data: numpy.ndarray, 形状为(3600,), 瓦斯浓度百分比(0.1%-2.0%)
    
    数据时间分布：
        0-1200秒: 平稳期1（正常开采）
        1200-1210秒: 模式A异常（突发尖峰）
        1210-1800秒: 平稳期2
        1800-2100秒: 模式B异常（缓慢涌出）
        2100-2700秒: 平稳期3
        2700-2750秒: 模式C异常（指数爆发）
        2750-3600秒: 平稳期4
    
    典型浓度范围：
        正常值: 0.1% - 0.5%
        预警阈值: 0.8%
        危险阈值: 1.5%
    ================================================================================
    """
    # 设置随机种子，确保每次运行生成相同的数据，便于实验复现
    np.random.seed(seed)
    
    # ============================================================================
    # 第一步：设置基础参数
    # ============================================================================
    # Fs: 采样频率，1Hz表示每秒采集1个数据点（符合煤矿监控系统实际采样频率）
    # T: 总采样时长，3600秒即1小时的数据量
    # t: 时间序列，从0开始，步长1/Fs，到T-1/Fs结束
    Fs = 1  # 采样频率 1Hz
    T = 3600  # 1小时，3600个数据点
    t = np.arange(0, T, 1/Fs)
    
    # ============================================================================
    # 第二步：生成平稳基线信号 + 高斯噪声
    # ============================================================================
    # base: 正常瓦斯浓度基线，设置为0.3%
    # np.random.randn(T): 生成T个标准正态分布随机数（均值0，标准差1）
    # 0.05 * np.random.randn(T): 添加幅度为0.05%的高斯噪声，模拟传感器测量误差
    base = 0.3 + 0.05 * np.random.randn(T)
    
    # ============================================================================
    # 第三步：添加周期性呼吸波动（采煤机作业影响）
    # ============================================================================
    # 采煤机周期性作业会产生类似呼吸的瓦斯浓度波动
    # sin(2*pi*t/300): 周期300秒（5分钟）的正弦波
    # 0.1: 波动幅度为0.1%
    breath = 0.1 * np.sin(2 * np.pi * t / 300)
    
    # ============================================================================
    # 第四步：添加三种典型异常模式
    # ============================================================================
    # 初始化异常信号数组
    anomalies = np.zeros(T)
    
    # -------------------------------------------------------------------------
    # 模式A：突发尖峰异常（放顶煤作业引起）
    # -------------------------------------------------------------------------
    # 发生时间：1200秒时刻
    # 物理原因：放顶煤作业时，顶板突然垮落，瓦斯瞬间涌出
    # 特征：10秒内从0.3%快速上升至1.1%，然后指数衰减
    # np.exp(-0.5 * np.arange(10)): 指数衰减序列，衰减系数0.5
    t_A_start = 1200  # 异常开始时间（秒）
    t_A_end = 1210    # 异常结束时间（秒）
    anomalies[t_A_start:t_A_end] = 0.8 * np.exp(-0.5 * np.arange(t_A_end - t_A_start))
    
    # -------------------------------------------------------------------------
    # 模式B：缓慢涌出异常（地质构造影响）
    # -------------------------------------------------------------------------
    # 发生时间：1800-2100秒时段（共300秒）
    # 物理原因：掘进工作面接近地质构造带（如断层、褶皱），瓦斯缓慢涌出
    # 特征：5分钟内线性增长至1.5%
    # np.arange(300): 0到299的线性序列
    # 0.005 * np.arange(300): 线性增长，最大值约1.5%
    t_B_start = 1800  # 异常开始时间（秒）
    t_B_end = 2100    # 异常结束时间（秒）
    anomalies[t_B_start:t_B_end] = 0.005 * np.arange(t_B_end - t_B_start)
    
    # -------------------------------------------------------------------------
    # 模式C：指数爆发异常（揭穿瓦斯包）
    # -------------------------------------------------------------------------
    # 发生时间：2700-2750秒时段（共50秒）
    # 物理原因：巷道掘进过程中意外揭穿瓦斯包，瓦斯高速涌出
    # 特征：50秒内指数增长至1.5%，比模式A更剧烈
    # (1 - np.exp(-0.1 * n)): 指数上升曲线，最终稳定在1.0附近
    t_C_start = 2700  # 异常开始时间（秒）
    t_C_end = 2750    # 异常结束时间（秒）
    anomalies[t_C_start:t_C_end] = 1.2 * (1 - np.exp(-0.1 * np.arange(t_C_end - t_C_start)))
    
    # ============================================================================
    # 第五步：添加传感器脉冲噪声
    # ============================================================================
    # 煤矿井下电磁干扰可能导致传感器产生脉冲噪声
    # 随机选择20个时间点添加幅度为0.3%的脉冲干扰
    pulse_noise = np.zeros(T)
    pulse_indices = np.random.choice(T, 20, replace=False)  # 随机选择20个不重复的位置
    pulse_noise[pulse_indices] = 0.3 * np.random.randn(20)  # 添加脉冲噪声
    
    # ============================================================================
    # 第六步：合成最终数据并限制范围
    # ============================================================================
    # 将所有分量相加：基线 + 呼吸波动 + 异常信号 + 脉冲噪声
    gas_data = base + breath + anomalies + pulse_noise
    # np.clip: 将瓦斯浓度限制在合理范围内（0.1% - 2.0%）
    # 低于0.1%视为传感器异常LOW，高于2.0%为危险HIGH
    gas_data = np.clip(gas_data, 0.1, 2.0)  # 限制范围
    
    return t, gas_data


