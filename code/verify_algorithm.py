def lttb_downsample(data, n_points):
    """
    ================================================================================
    经典LTTB下采样算法（固定桶大小）
    ================================================================================
    
    算法原理：
        LTTB（Largest Triangle Three Buckets）是一种保形压缩算法，
        其核心思想是将数据划分为若干桶，每个桶内选择一个代表点，使得
        该点与前后桶的代表点构成的三角形面积最大，从而最大程度保留
        数据的形态特征。
    
    本函数实现：
        采用固定桶大小的经典LTTB算法，作为对比基准。
        固定桶意味着无论数据内容如何，每个桶的大小都相同，这会导致
        在数据变化平缓的区域采样点冗余，而在数据变化剧烈的区域
        采样点不足。
    
    参数说明：
        data (numpy.ndarray): 输入的原始数据序列，一维数组
        n_points (int): 目标输出点数，即希望保留的关键点数量
    
    返回值：
        tuple: (采样点索引数组, 采样点数据值数组)
            - sampled_indices: 采样点在原始数据中的索引位置
            - data[sampled_indices]: 对应的数据值
    
    算法步骤：
        1. 计算固定桶大小：bucket_size = (n-2) / (n_points-2)
        2. 始终保留第一个和最后一个数据点
        3. 对每个中间桶：
           a. 确定桶内的候选点
           b. 计算每个候选点与前一点、下一桶平均值构成的三角形面积
           c. 选择面积最大的点作为该桶的代表点
    
    适用场景：
        - 数据变化相对均匀的场景
        - 作为基准算法与其他动态分桶方法对比
    
    局限性：
        - 无法自适应数据变化，在异常检测场景下效果较差
    ================================================================================
    """
    # 获取输入数据的长度
    n = len(data)
    
    # 边界情况处理：如果目标点数大于等于原始数据点数，直接返回全部数据
    if n_points >= n:
        return np.arange(n), data
    
    # ============================================================================
    # 第一步：计算固定桶大小
    # ============================================================================
    # 公式：bucket_size = (n - 2) / (n_points - 2)
    # 解释：
    #   - n-2: 去掉首尾两个保留点，剩余n-2个点需要分配
    #   - n_points-2: 需要采样n_points个点，去掉首尾，剩余n_points-2个桶
    #   - 结果是每个桶包含的平均点数（固定值）
    bucket_size = (n - 2) / (n_points - 2)
    
    # ============================================================================
    # 第二步：初始化采样点列表，始终保留第一个点
    # ============================================================================
    # 煤矿安全监控强调首尾数据必须保留，确保时序连续性
    sampled_indices = [0]  # 第一个索引（时间起点）
    
    # ============================================================================
    # 第三步：遍历每个桶，选择最佳代表点
    # ============================================================================
    # range(1, n_points-1): 跳过第一个点，从第二个点开始遍历到倒数第二个点
    for i in range(1, n_points - 1):
        # --------------------------------------------------------------------
        # 3.1 计算当前桶的索引范围
        # --------------------------------------------------------------------
        # bucket_start: 桶的起始索引（包含）
        # bucket_end: 桶的结束索引（不包含）
        bucket_start = int((i - 1) * bucket_size) + 1
        bucket_end = int(i * bucket_size) + 1
        
        # --------------------------------------------------------------------
        # 3.2 获取参考点：前一个已选中的采样点
        # --------------------------------------------------------------------
        # 这就是LTTB算法"三点"中的第一个点（P1）
        previous_point = data[sampled_indices[-1]]
        
        # --------------------------------------------------------------------
        # 3.3 计算下一个桶的范围，用于确定三角形"第三点"
        # --------------------------------------------------------------------
        # 注意：这里用下一个桶的平均值作为参考，而不是具体的点
        # 这是LTTB算法的关键优化，减少计算量
        next_bucket_start = int(i * bucket_size) + 1
        next_bucket_end = int((i + 1) * bucket_size) + 1
        
        # 边界保护：确保不超出数据范围
        if next_bucket_end > n - 1:
            next_bucket_end = n - 1
        
        # 计算下一个桶内所有数据点的平均值（P3点）
        next_bucket_avg = np.mean(data[next_bucket_start:next_bucket_end])
        
        # --------------------------------------------------------------------
        # 3.4 在当前桶内寻找最佳代表点（最大三角形面积）
        # --------------------------------------------------------------------
        max_area = -1  # 初始化最大面积为负数
        best_idx = bucket_start  # 初始化最佳索引为桶起始位置
        
        # 遍历桶内所有候选点
        for j in range(bucket_start, min(bucket_end, n - 1)):
            # 计算三角形面积（叉积法）
            # 三角形三个点：
            #   P1 = (sampled_indices[-1], previous_point) - 前一个采样点
            #   P2 = (j, data[j]) - 当前候选点
            #   P3 = (next_bucket_start, next_bucket_avg) - 下一桶平均值
            # 面积公式：|AB × AC| / 2（简化后）
            area = abs((previous_point - next_bucket_avg) * (j - sampled_indices[-1]) - 
                       (data[j] - previous_point) * (next_bucket_start - sampled_indices[-1]))
            
            # 更新最大面积和最佳索引
            if area > max_area:
                max_area = area
                best_idx = j
        
        # 将最佳点加入采样结果
        sampled_indices.append(best_idx)
    
    # ============================================================================
    # 第四步：确保最后一个点被保留
    # ============================================================================
    # 煤矿安全监控强调时序数据的完整性，末尾点同样重要
    sampled_indices.append(n - 1)
    
    # ============================================================================
    # 第五步：返回采样结果
    # ============================================================================
    return np.array(sampled_indices), data[sampled_indices]


def dynamic_lttb_gradient(data, params):
    """
    ================================================================================
    实施例一：基于梯度的动态LTTB算法
    ================================================================================
    
    发明名称（对应技术交底书）：
        基于梯度变化的动态LTTB桶划分方法
    
    技术方案概述：
        本算法通过计算数据序列的梯度（变化率），动态调整每个桶的大小。
        当数据变化剧烈（梯度大）时，缩小桶以捕获更多细节；
        当数据变化平缓（梯度小）时，放大桶以节省存储空间。
    
    技术效果：
        - 在瓦斯浓度突变区域（如模式A突发尖峰）自动增加采样密度
        - 在平稳期保持较低采样率，节省传输带宽
        - 实现数据压缩率与异常检测能力的平衡
    
    参数说明：
        data (numpy.ndarray): 输入的原始瓦斯浓度数据序列
        params (dict): 算法参数字典，包含以下键：
            - M (int): 目标输出点数，默认为72（即每小时72个点，30秒一个点）
            - B_base (int): 基础桶大小，默认为50
            - alpha (float): 梯度灵敏度因子，默认为1.0
                - alpha越大，对梯度变化越敏感
                - alpha=0时退化为固定桶
            - B_min (int): 桶大小下限，默认为5（异常区域最小桶）
            - B_max (int): 桶大小上限，默认为100（平稳区域最大桶）
    
    返回值：
        tuple: (采样点索引数组, 滤波后采样点数据数组)
    
    算法流程：
        1. 抗噪预处理：使用中值滤波消除传感器脉冲噪声
        2. 计算梯度：使用前向差分计算数据变化率
        3. 动态分桶：根据梯度自适应调整桶大小
        4. LTTB采样：在每个桶内选择最大三角形面积的点
    
    核心公式：
        B_i = B_base / (1 + alpha * g_avg)
        
        其中：
        - B_i: 第i个桶的动态大小
        - B_base: 基础桶大小
        - alpha: 梯度灵敏度因子
        - g_avg: 当前区域的平均梯度值
    
    发明点说明：
        本算法将数据本身的变化特征（梯度）与LTTB的桶划分相结合，
        实现了"数据自适应"的压缩采样，是本发明的核心创新点之一。
    ================================================================================
    """
    # ============================================================================
    # 提取算法参数，设置默认值（如果未提供）
    # ============================================================================
    M = params.get('M', 72)        # 目标输出点数
    B_base = params.get('B_base', 50)  # 基础桶大小
    alpha = params.get('alpha', 1.0)   # 梯度灵敏度因子
    B_min = params.get('B_min', 5)     # 桶大小下限
    B_max = params.get('B_max', 100)   # 桶大小上限
    
    # 获取数据长度
    n = len(data)
    
    # ============================================================================
    # 第一步：抗噪预处理（中值滤波）
    # ============================================================================
    # 煤矿井下环境复杂，传感器容易受到电磁干扰产生脉冲噪声
    # 中值滤波是一种非线性滤波方法，能有效消除脉冲噪声同时保留边缘
    # kernel_size=3: 使用3点中值滤波，既能去噪又不至于过度平滑
    from scipy.signal import medfilt  # 从scipy导入中值滤波函数
    data_filtered = medfilt(data, kernel_size=3)
    
    # ============================================================================
    # 第二步：计算梯度序列
    # ============================================================================
    # np.diff: 计算前向差分，即相邻两点之间的差值
    # np.abs: 取绝对值，因为只关心变化的幅度，不关心方向
    # G[i] 表示data_filtered[i+1] - data_filtered[i]的绝对值
    G = np.abs(np.diff(data_filtered))  # 前向差分
    
    # ============================================================================
    # 第三步：动态分桶（基于梯度）
    # ============================================================================
    # 核心创新：根据数据梯度动态调整桶大小
    # 梯度大的区域（变化剧烈）-> 桶小 -> 采样点多
    # 梯度小的区域（变化平缓）-> 桶大 -> 采样点少
    
    avg_gradients = []  # 存储每个区域的平均梯度（可选用于分析）
    bucket_boundaries = [0]  # 桶边界数组，第一个边界是0（数据起点）
    
    remaining_points = n - 2  # 剩余需要分配的点（去掉首尾）
    
    # 遍历M个桶，计算每个桶的动态大小
    for i in range(1, M):
        # --------------------------------------------------------------------
        # 3.1 计算当前区域的平均梯度
        # --------------------------------------------------------------------
        idx = min(i, len(G))  # 确保索引不超出梯度数组范围
        if idx > 0:
            # 计算从上一个边界到当前位置的平均梯度
            g_avg = np.mean(G[max(0, bucket_boundaries[-1]):idx])
        else:
            g_avg = 0  # 初始区域无梯度，设为0
        
        avg_gradients.append(g_avg)
        
        # --------------------------------------------------------------------
        # 3.2 动态桶公式：B_i = B_base / (1 + alpha * g_avg)
        # --------------------------------------------------------------------
        # 公式解释：
        #   - 当g_avg=0（完全平稳）时，B_i = B_base（最大桶）
        #   - 当g_avg增大时，分母增大，B_i减小（桶变小）
        #   - alpha控制灵敏度，alpha越大，梯度对桶大小的影响越显著
        B_i_raw = B_base / (1 + alpha * g_avg)
        
        # 使用np.clip限制桶大小在[B_min, B_max]范围内
        # B_min=5: 异常区域最多分成5个点的桶，确保足够的细节
        # B_max=100: 平稳区域最多100个点一个桶，节省存储
        if np.isnan(B_i_raw) or np.isinf(B_i_raw):
            B_i_raw = B_min
        B_i = int(np.clip(B_i_raw, B_min, B_max))
        
        # --------------------------------------------------------------------
        # 3.3 更新桶边界
        # --------------------------------------------------------------------
        new_boundary = min(bucket_boundaries[-1] + B_i, n - 1)
        bucket_boundaries.append(new_boundary)
        
        # 如果已经到达数据末尾，提前结束
        if new_boundary >= n - 1:
            break
    
    # ============================================================================
    # 第四步：确保边界完整性
    # ============================================================================
    # 强制将最后一个边界设置为数据终点，确保时序完整
    bucket_boundaries[-1] = n - 1
    
    # ============================================================================
    # 第五步：LTTB采样（在动态划分的桶内）
    # ============================================================================
    # 对每个桶执行经典的LTTB采样：选择最大三角形面积的点
    sampled_indices = [0]  # 始终保留第一个点
    
    # 遍历所有桶（除了最后一个，因为最后一个点是特殊处理的）
    for i in range(1, len(bucket_boundaries) - 1):
        # 确定当前桶的范围
        bucket_start = bucket_boundaries[i - 1] + 1
        bucket_end = bucket_boundaries[i]
        
        # 跳过无效桶（空桶）
        if bucket_end <= bucket_start:
            continue
        
        # 确定下一个桶的范围（用于计算三角形面积）
        next_bucket_start = bucket_end
        next_bucket_end = bucket_boundaries[i + 1]
        
        # 边界保护
        if next_bucket_end <= next_bucket_start:
            next_bucket_end = min(next_bucket_start + 1, n - 1)
        
        # 计算下一个桶的平均值（作为三角形的第三个参考点）
        next_bucket_avg = np.mean(data_filtered[next_bucket_start:next_bucket_end])
        
        # --------------------------------------------------------------------
        # 在当前桶内寻找最佳代表点（最大三角形面积）
        # --------------------------------------------------------------------
        previous_point = data_filtered[sampled_indices[-1]]  # 前一个采样点
        max_area = -1
        best_idx = bucket_start
        
        # 遍历桶内所有候选点
        for j in range(bucket_start, min(bucket_end, n - 1)):
            # 计算三角形面积（与经典LTTB相同）
            area = abs((previous_point - next_bucket_avg) * (j - sampled_indices[-1]) - 
                     (data_filtered[j] - previous_point) * (next_bucket_start - sampled_indices[-1]))
            if area > max_area:
                max_area = area
                best_idx = j
        
        sampled_indices.append(best_idx)
    
    # 始终保留最后一个点
    sampled_indices.append(n - 1)
    
    # ============================================================================
    # 第六步：返回结果
    # ============================================================================
    # 返回采样点索引和对应的滤波后数据值
    return np.array(sampled_indices), data_filtered[sampled_indices]


def dynamic_lttb_variance(data, params):
    """
    ================================================================================
    实施例二：基于滑动方差的动态LTTB算法
    ================================================================================
    
    发明名称（对应技术交底书）：
        基于滑动方差分析的动态LTTB桶划分方法
    
    技术方案概述：
        本算法通过计算数据序列的滑动方差（波动程度），动态调整桶大小。
        方差反映了数据的离散程度或波动幅度：
        - 高方差区域：数据波动剧烈，需要更多采样点
        - 低方差区域：数据相对平稳，可以减少采样点
    
    技术效果：
        - 方差对缓慢变化（模式B缓慢涌出）比梯度更敏感
        - 三区域划分（平稳区/过渡区/异常区）使调整更平滑
        - 滑动窗口设计能够捕获局部波动特征
    
    参数说明：
        data (numpy.ndarray): 输入的原始瓦斯浓度数据序列
        params (dict): 算法参数字典，包含以下键：
            - M (int): 目标输出点数，默认为72
            - W (int): 滑动窗口大小，默认为30
                - 窗口越大，统计越稳定但响应越慢
                - 窗口越小，响应越快但可能受噪声影响
            - Th_low (float): 低方差阈值，默认为0.02
                - 方差低于此值视为平稳区
            - Th_high (float): 高方差阈值，默认为0.2
                - 方差高于此值视为异常区
            - B_min (int): 桶大小下限，默认为5
            - B_max (int): 桶大小上限，默认为100
            - beta (float): 平滑系数，默认为0.5
                - 用于平滑桶大小的过渡变化，避免突变
    
    返回值：
        tuple: (采样点索引数组, 滤波后采样点数据数组)
    
    算法流程：
        1. 抗噪预处理：中值滤波消除脉冲噪声
        2. 计算滑动方差：使用滑动窗口计算每点的方差
        3. 三区域判断：根据方差与阈值的关系划分区域
        4. 动态分桶：根据区域调整桶大小
        5. LTTB采样：在每个桶内选择最佳代表点
    
    三区域划分逻辑：
        ┌─────────────────────────────────────────────────────────┐
        │  Var < Th_low  │  Th_low ≤ Var ≤ Th_high  │ Var > Th_high │
        ├────────────────┼──────────────────────────┼───────────────┤
        │    平稳区       │       过渡区             │    异常区      │
        │   桶=B_max     │  线性插值(B_max→B_min)   │   桶=B_min    │
        └─────────────────────────────────────────────────────────┘
    
    发明点说明：
        - 引入方差作为数据波动的度量，比梯度更适合检测缓慢变化
        - 三区域划分和线性插值使桶大小调整更加平滑自然
        - 滑动窗口设计能够适应非平稳的瓦斯数据特征
    ================================================================================
    """
    # ============================================================================
    # 提取算法参数
    # ============================================================================
    M = params.get('M', 72)          # 目标输出点数
    W = params.get('W', 30)          # 滑动窗口大小
    Th_low = params.get('Th_low', 0.02)    # 低方差阈值
    Th_high = params.get('Th_high', 0.2)   # 高方差阈值
    B_min = params.get('B_min', 5)    # 桶大小下限
    B_max = params.get('B_max', 100)  # 桶大小上限
    beta = params.get('beta', 0.5)    # 平滑系数
    
    n = len(data)
    
    # ============================================================================
    # 第一步：抗噪预处理（中值滤波）
    # ============================================================================
    from scipy.signal import medfilt
    data_filtered = medfilt(data, kernel_size=3)
    
    # ============================================================================
    # 第二步：计算滑动方差
    # ============================================================================
    # 滑动方差反映数据在局部区域的波动程度
    # Var[i] 表示以i为中心、窗口大小为W的区域内的方差
    Var = np.zeros(n)
    half_W = W // 2  # 窗口半宽
    
    # 遍历每个数据点，计算滑动方差
    for i in range(n):
        # 确定窗口边界（考虑边界情况）
        start = max(0, i - half_W)    # 窗口起始点
        end = min(n, i + half_W)      # 窗口结束点（不包含）
        # 计算窗口内数据的方差
        Var[i] = np.var(data_filtered[start:end])
    
    # ============================================================================
    # 第三步：动态分桶（三区域划分）
    # ============================================================================
    bucket_boundaries = [0]  # 桶边界数组
    B_smooth = B_max  # 平滑后的桶大小，初始设为最大值
    
    # 遍历M-1个桶
    for i in range(M - 1):
        # --------------------------------------------------------------------
        # 3.1 获取当前位置的方差
        # --------------------------------------------------------------------
        current_pos = bucket_boundaries[-1]  # 当前边界位置
        if current_pos < n:
            current_var = Var[current_pos]  # 当前位置的方差
        else:
            current_var = 0
        
        # --------------------------------------------------------------------
        # 3.2 三区域判断逻辑
        # --------------------------------------------------------------------
        if current_var < Th_low:
            # 平稳区：方差小于低阈值，数据波动小
            # 使用最大桶，减少冗余采样
            B_i = B_max
            
        elif current_var > Th_high:
            # 异常区：方差大于高阈值，数据波动剧烈
            # 使用最小桶，确保捕获异常细节
            B_i = B_min
            
        else:
            # 过渡区：方差在两个阈值之间
            # 使用线性插值计算桶大小，实现平滑过渡
            # ratio: 0到1之间的比例，表示当前方差在阈值区间的位置
            ratio = (current_var - Th_low) / (Th_high - Th_low)
            # 当ratio=0时，B_i=B_max（接近平稳区）
            # 当ratio=1时，B_i=B_min（接近异常区）
            B_i = int(B_max - (B_max - B_min) * ratio)
        
        # --------------------------------------------------------------------
        # 3.3 平滑处理（指数移动平均）
        # --------------------------------------------------------------------
        # 使用平滑系数beta避免桶大小突变
        # B_smooth = beta * B_i + (1 - beta) * B_smooth
        # beta=0.5: 50%新值 + 50%历史值，平滑效果适中
        B_smooth = beta * B_i + (1 - beta) * B_smooth
        B_i = int(B_smooth)
        
        # --------------------------------------------------------------------
        # 3.4 更新桶边界
        # --------------------------------------------------------------------
        new_boundary = min(bucket_boundaries[-1] + B_i, n - 1)
        bucket_boundaries.append(new_boundary)
        
        if new_boundary >= n - 1:
            break
    
    # 确保最后一点被包含
    bucket_boundaries[-1] = n - 1
    
    # ============================================================================
    # 第四步：LTTB采样（与前面相同）
    # ============================================================================
    sampled_indices = [0]
    
    for i in range(1, len(bucket_boundaries) - 1):
        bucket_start = bucket_boundaries[i - 1] + 1
        bucket_end = bucket_boundaries[i]
        
        if bucket_end <= bucket_start:
            continue
        
        next_bucket_start = bucket_end
        next_bucket_end = bucket_boundaries[i + 1]
        
        if next_bucket_end <= next_bucket_start:
            next_bucket_end = min(next_bucket_start + 1, n - 1)
        
        next_bucket_avg = np.mean(data_filtered[next_bucket_start:next_bucket_end])
        
        previous_point = data_filtered[sampled_indices[-1]]
        max_area = -1
        best_idx = bucket_start
        
        for j in range(bucket_start, min(bucket_end, n - 1)):
            area = abs((previous_point - next_bucket_avg) * (j - sampled_indices[-1]) - 
                      (data_filtered[j] - previous_point) * (next_bucket_start - sampled_indices[-1]))
            if area > max_area:
                max_area = area
                best_idx = j
        
        sampled_indices.append(best_idx)
    
    sampled_indices.append(n - 1)
    
    return np.array(sampled_indices), data_filtered[sampled_indices]


def dynamic_lttb_fusion(data, params):
    """
    ================================================================================
    实施例三：基于多参数融合的动态LTTB算法
    ================================================================================
    
    发明名称（对应技术交底书）：
        基于梯度与方差融合的动态LTTB桶划分方法
    
    技术方案概述：
        本算法是实施例一和实施例二的融合版本，同时考虑梯度和方差两个指标。
        通过加权融合的方式，综合利用：
        - 梯度：对突变型异常（如模式A突发尖峰、模式C指数爆发）敏感
        - 方差：对缓变型异常（如模式B缓慢涌出）敏感
        
        融合后的评分R综合了两种指标的优点，能够更全面地检测各类异常。
    
    技术效果：
        - 对各类异常模式都具有良好的检测能力
        - 梯度+方差的组合比单一指标更鲁棒
        - 适应更复杂的瓦斯涌出场景
    
    参数说明：
        data (numpy.ndarray): 输入的原始瓦斯浓度数据序列
        params (dict): 算法参数字典，包含以下键：
            - M (int): 目标输出点数，默认为72
            - w1 (float): 梯度权重，默认为0.6
                - 梯度权重较高，因为梯度对快速变化更敏感
            - w2 (float): 方差权重，默认为0.4
                - 方差权重较低，作为梯度的补充
            - B_base (int): 基础桶大小，默认为50
            - alpha (float): 灵敏度因子，默认为1.0
            - B_min (int): 桶大小下限，默认为5
            - B_max (int): 桶大小上限，默认为100
            - W (int): 滑动窗口大小，默认为30
    
    权重设置原则：
        w1 + w2 = 1.0
        - w1 > w2: 因为梯度对煤矿瓦斯的突发异常更直接有效
        - 实际应用中可根据矿井具体情况进行调整
    
    返回值：
        tuple: (采样点索引数组, 滤波后采样点数据数组)
    
    算法流程：
        1. 抗噪预处理：中值滤波
        2. 计算梯度：前向差分
        3. 计算方差：滑动窗口
        4. 归一化处理：将梯度和方差归一化到[0,1]区间
        5. 加权融合：R = w1 * G_norm + w2 * Var_norm
        6. 动态分桶：根据融合评分调整桶大小
        7. LTTB采样：在每个桶内选择最佳代表点
    
    融合公式：
        R[i] = w1 * G_norm[i] + w2 * Var_norm[i]
        
        其中：
        - G_norm: 归一化后的梯度序列
        - Var_norm: 归一化后的方差序列
        - w1, w2: 权重系数，且w1 + w2 = 1
    
    发明点说明：
        - 提出多参数融合的思路，综合利用不同指标的优势
        - 归一化处理确保不同量纲的指标可以公平比较
        - 加权融合策略可以根据实际应用场景灵活调整
    ================================================================================
    """
    # ============================================================================
    # 提取算法参数
    # ============================================================================
    M = params.get('M', 72)          # 目标输出点数
    w1 = params.get('w1', 0.6)        # 梯度权重（占60%）
    w2 = params.get('w2', 0.4)        # 方差权重（占40%）
    B_base = params.get('B_base', 50) # 基础桶大小
    alpha = params.get('alpha', 1.0)  # 灵敏度因子
    B_min = params.get('B_min', 5)     # 桶大小下限
    B_max = params.get('B_max', 100)   # 桶大小上限
    W = params.get('W', 30)           # 滑动窗口大小
    
    n = len(data)
    
    # ============================================================================
    # 第一步：抗噪预处理（中值滤波）
    # ============================================================================
    from scipy.signal import medfilt
    data_filtered = medfilt(data, kernel_size=3)
    
    # ============================================================================
    # 第二步：计算梯度并进行归一化处理
    # ============================================================================
    # 计算前向差分梯度
    G = np.abs(np.diff(data_filtered))
    # 归一化到[0,1]区间
    # np.max(G) + 1e-10: 加上小常数避免除零错误
    G_norm = G / (np.max(G) + 1e-10)
    
    # ============================================================================
    # 第三步：计算滑动方差并进行归一化处理
    # ============================================================================
    Var = np.zeros(n)
    half_W = W // 2
    
    # 遍历每个数据点，计算滑动方差
    for i in range(n):
        start = max(0, i - half_W)
        end = min(n, i + half_W)
        Var[i] = np.var(data_filtered[start:end])
    
    # 归一化到[0,1]区间
    Var_norm = Var / (np.max(Var) + 1e-10)
    
    # ============================================================================
    # 第四步：计算融合评分R
    # ============================================================================
    # 融合公式：R[i] = w1 * G_norm[i] + w2 * Var_norm[i]
    # 
    # 设计原理：
    #   - 梯度对突变敏感（模式A、C）
    #   - 方差对缓变敏感（模式B）
    #   - 加权融合综合两种指标的优点
    # 
    # 当G_norm高且Var_norm也高时，R值最高，说明是明显的异常区域
    R = np.zeros(n)
    for i in range(min(len(G_norm), len(Var_norm))):
        R[i] = w1 * G_norm[i] + w2 * Var_norm[i]
    
    # ============================================================================
    # 第五步：基于融合评分的动态分桶
    # ============================================================================
    # 原理与实施例一相同，但使用融合评分R替代梯度G
    # R越高，数据变化越剧烈，桶越小
    bucket_boundaries = [0]
    
    for i in range(1, M):
        idx = min(i, len(R))
        if idx > 0:
            # 计算当前区域的平均融合评分
            r_avg = np.mean(R[max(0, bucket_boundaries[-1]):idx])
        else:
            r_avg = 0
        
        # 动态桶公式：B_i = B_base / (1 + alpha * r_avg)
        B_i_raw = B_base / (1 + alpha * r_avg)
        if np.isnan(B_i_raw) or np.isinf(B_i_raw):
            B_i_raw = B_min
        B_i = int(np.clip(B_i_raw, B_min, B_max))
        
        new_boundary = min(bucket_boundaries[-1] + B_i, n - 1)
        bucket_boundaries.append(new_boundary)
        
        if new_boundary >= n - 1:
            break
    
    # 确保最后一点被包含
    bucket_boundaries[-1] = n - 1
    
    # ============================================================================
    # 第六步：LTTB采样（与前面相同）
    # ============================================================================
    sampled_indices = [0]
    
    for i in range(1, len(bucket_boundaries) - 1):
        bucket_start = bucket_boundaries[i - 1] + 1
        bucket_end = bucket_boundaries[i]
        
        if bucket_end <= bucket_start:
            continue
        
        next_bucket_start = bucket_end
        next_bucket_end = bucket_boundaries[i + 1]
        
        if next_bucket_end <= next_bucket_start:
            next_bucket_end = min(next_bucket_start + 1, n - 1)
        
        next_bucket_avg = np.mean(data_filtered[next_bucket_start:next_bucket_end])
        
        previous_point = data_filtered[sampled_indices[-1]]
        max_area = -1
        best_idx = bucket_start
        
        for j in range(bucket_start, min(bucket_end, n - 1)):
            area = abs((previous_point - next_bucket_avg) * (j - sampled_indices[-1]) - 
                      (data_filtered[j] - previous_point) * (next_bucket_start - sampled_indices[-1]))
            if area > max_area:
                max_area = area
                best_idx = j
        
        sampled_indices.append(best_idx)
    
    sampled_indices.append(n - 1)
    
    return np.array(sampled_indices), data_filtered[sampled_indices]


