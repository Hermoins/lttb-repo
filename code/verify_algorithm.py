def calculate_metrics(original_data, sampled_indices, sampled_data, original_time, method_name):
    """
    ================================================================================
    计算评估指标
    ================================================================================
    
    功能说明：
        对采样算法的性能进行全面评估，计算多个关键指标。
    
    参数说明：
        original_data (numpy.ndarray): 原始数据序列
        sampled_indices (numpy.ndarray): 采样点的索引位置
        sampled_data (numpy.ndarray): 采样后的数据值
        original_time (numpy.ndarray): 原始数据的时间序列
        method_name (str): 算法名称（用于显示）
    
    返回值：
        dict: 包含以下评估指标的字典：
            - compression_ratio: 压缩比（原始点数/采样点数）
            - feature_retainment: 关键特征保留率
            - rmse: 均方根误差
            - region_counts: 各时间段采样点分布
    
    评估指标详解：
        1. 压缩比（compression_ratio）：
           - 定义：n_original / n_sampled
           - 含义：原始数据被压缩的程度
           - 理想值：越高越好，表示压缩力度大
        
        2. 关键特征保留率（feature_retainment）：
           - 定义：采样后保留的关键特征点数量 / 原始关键特征点总数
           - 关键特征：峰值点（超过均值+2倍标准差的局部极大值）
           - 含义：对异常检测重要的特征是否被保留
           - 理想值：接近1.0，表示异常特征未被遗漏
        
        3. RMSE（均方根误差）：
           - 定义：sqrt(mean((original - reconstructed)^2))
           - 计算方法：将采样点线性插值回原始长度后与原数据对比
           - 含义：采样重建的精度
           - 理想值：越低越好，表示重建误差小
        
        4. 区域采样点分布（region_counts）：
           - 定义：各时间段内采样点的数量
           - 时段划分：
             * stable1: 0-1200秒（平稳期1）
             * modeA: 1200-1210秒（模式A异常）
             * stable2: 1210-1800秒（平稳期2）
             * modeB: 1800-2100秒（模式B异常）
             * stable3: 2100-2700秒（平稳期3）
             * modeC: 2700-2750秒（模式C异常）
           - 含义：采样点是否集中分布在异常区域
    
    注意事项：
        - 特征保留率使用容差机制（tolerance=5），允许5个点的偏差
        - 这是因为LTTB是近似算法，不可能精确捕获每个特征点
    ================================================================================
    """
    # ============================================================================
    # 获取数据长度
    # ============================================================================
    n_original = len(original_data)
    n_sampled = len(sampled_data)
    
    # 初始化结果字典
    results = {}
    
    # ============================================================================
    # 指标1：计算压缩比
    # ============================================================================
    # 压缩比 = 原始数据点数 / 采样后数据点数
    # 例如：3600 / 72 = 50，表示原始数据被压缩了50倍
    results['compression_ratio'] = n_original / n_sampled
    
    # ============================================================================
    # 指标2：计算关键特征保留率
    # ============================================================================
    # 2.1 确定峰值阈值：均值 + 2倍标准差
    # 使用统计方法自动识别异常峰值，避免人工设定阈值
    threshold = np.mean(original_data) + 2 * np.std(original_data)
    
    # 2.2 识别原始数据中的峰值点
    # 峰值条件：
    #   - 当前值 > 阈值（是异常值）
    #   - 当前值 > 前一个值（是局部极大值）
    #   - 当前值 > 后一个值（是局部极大值）
    peaks_original = []
    for i in range(1, n_original - 1):
        if original_data[i] > threshold and original_data[i] > original_data[i-1] and original_data[i] > original_data[i+1]:
            peaks_original.append(i)
    
    # 2.3 检查采样数据是否保留了这些峰值
    retained = 0
    tolerance = 5  # 允许5个点的偏差
    # 原因：LTTB是近似算法，采样点位置可能略有偏移
    
    # 遍历每个原始峰值，检查是否在采样点附近找到对应点
    for peak in peaks_original:
        for sampled_idx in sampled_indices:
            if abs(peak - sampled_idx) <= tolerance:
                retained += 1  # 找到对应的采样点
                break  # 一个峰值只需要匹配一个采样点
    
    # 2.4 计算保留率
    # 保留率 = 保留的峰值数 / 原始峰值总数
    results['feature_retainment'] = retained / len(peaks_original) if peaks_original else 1.0
    
    # ============================================================================
    # 指标3：计算RMSE（均方根误差）
    # ============================================================================
    # 3.1 导入插值函数
    from scipy.interpolate import interp1d
    
    # 3.2 创建线性插值函数
    # 将离散的采样点插值成连续曲线，以便与原始数据对比
    # kind='linear': 线性插值
    # fill_value='extrapolate': 允许外推，处理边界情况
    f = interp1d(sampled_indices, sampled_data, kind='linear', fill_value='extrapolate')
    
    # 3.3 在原始时间点上计算插值结果
    sampled_interpolated = f(np.arange(n_original))
    
    # 3.4 计算均方根误差
    # RMSE = sqrt(mean((y_true - y_pred)^2))
    rmse = np.sqrt(np.mean((original_data - sampled_interpolated) ** 2))
    results['rmse'] = rmse
    
    # ============================================================================
    # 指标4：计算各区域采样点分布
    # ============================================================================
    # 4.1 定义时间段划分
    # 根据模拟数据中的异常模式划分区域
    regions = {
        'stable1': (0, 1200),      # 平稳期1：0-1200秒
        'modeA': (1200, 1210),     # 模式A：突发尖峰（1200-1210秒）
        'stable2': (1210, 1800),    # 平稳期2：1210-1800秒
        'modeB': (1800, 2100),     # 模式B：缓慢涌出（1800-2100秒）
        'stable3': (2100, 2700),   # 平稳期3：2100-2700秒
        'modeC': (2700, 2750),     # 模式C：指数爆发（2700-2750秒）
    }
    
    # 4.2 统计每个区域的采样点数量
    results['region_counts'] = {}
    for region_name, (start, end) in regions.items():
        # 统计落在[start, end)区间内的采样点数量
        count = np.sum((sampled_indices >= start) & (sampled_indices < end))
        results['region_counts'][region_name] = count
    
    # ============================================================================
    # 返回所有评估指标
    # ============================================================================
    return results


